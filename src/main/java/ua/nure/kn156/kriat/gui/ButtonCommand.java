package ua.nure.kn156.kriat.gui;

/**
 * The {@code ButtonCommand} class represents commands for buttons,
 * that represents as string.
 * @author Vadym Kriat
 */
public class ButtonCommand {
    /**
     * Return value if OK is chosen
     */
    public static final String OK = "ok";
    /**
     * Return value if CANCEL is chosen
     */
    public static final String CANCEL = "cancel";
    /**
     * Return value if CLOSE is chosen
     */
    public static final String CLOSE = "close";
    /**
     * Return value if ADD is chosen
     */
    public static final String ADD = "add";
    /**
     * Return value if DELETE is chosen
     */
    public static final String DELETE = "delete";
    /**
     * Return value if EDIT is chosen
     */
    public static final String EDIT = "edit";
    /**
     * Return value if DETAILS is chosen
     */
    public static final String DETAILS = "details";
}
